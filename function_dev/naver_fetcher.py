# agents/naver_fetcher.py

import requests
from dotenv import load_dotenv
import os
from datetime import datetime, timedelta

# .env에서 불러오기
load_dotenv()
NAVER_CLIENT_ID = os.getenv("NAVER_CLIENT_ID")
NAVER_CLIENT_SECRET = os.getenv("NAVER_CLIENT_SECRET")

def fetch_naver_blog(query, display=10, days=0):
    url = "https://openapi.naver.com/v1/search/blog.json"
    params = {
        "query": query,
        "display": display,
        "sort": "date",  # 최신순 정렬
    }
    headers = {
        "X-Naver-Client-Id": NAVER_CLIENT_ID,
        "X-Naver-Client-Secret": NAVER_CLIENT_SECRET
    }

    response = requests.get(url, headers=headers, params=params)
    if response.status_code == 200:
        data = response.json()
        results = []
        for item in data.get('items', []):
            postdate_str = item.get('postdate')
            postdate = datetime.strptime(postdate_str, '%Y%m%d')

            # 날짜 필터링 (사용자가 days > 0으로 지정한 경우만)
            if days > 0:
                limit_date = datetime.now() - timedelta(days=days)
                if postdate < limit_date:
                    continue  # 해당 기간 이외면 스킵

            results.append({
                'title': item.get('title'),
                'link': item.get('link'),
                'description': item.get('description'),
                'bloggername': item.get('bloggername'),
                'postdate': postdate.strftime('%Y-%m-%d')
            })
        print(f"✅ {len(results)}건 수집 완료.")
        return results
    else:
        print(f"❌ 요청 실패: {response.status_code}, {response.text}")
        return []

if __name__ == "__main__":
    # 1일 이내 결과만 필터링
    blogs = fetch_naver_blog("인공지능", display=100, days=1)
    for idx, blog in enumerate(blogs, 1):
        print(f"\n[{idx}] {blog['title']} ({blog['postdate']})")
        print(f"- {blog['description']}")
        print(f"- {blog['link']}")


